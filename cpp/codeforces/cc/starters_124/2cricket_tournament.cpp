#include <bits/stdc++.h>
using namespace std;
// https://www.codechef.com/START124D/problems/CRICKETUDYAM

void solve(){
    int n, m;
    cin >> n >> m;
    if(m>=n){
        cout << "NO\n";
    }
    else{
        cout << "YES\n";
    }
}

int main(){
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    cout.tie(0);

    int t;
    cin >> t;

    while(t--){
        solve();
    }

    return 0;
}